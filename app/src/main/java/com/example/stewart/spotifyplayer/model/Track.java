public class Track {

    private Album album;
    private String id;
    private String name;
    private String uri;

    public Album getAlbum() {
        return album;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUri() {
        return uri;
    }


}
